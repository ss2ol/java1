
public class DataType1 {

	public static void main(String[] args) {
		int age = 29;
		System.out.println(age);
		
		//int age = 33;
		
		age = 33;
		
		String name = "진솔";
		System.out.println(name);

		//문자 - 실제로는 정수로 저장 
		char ch ='A';
		System.out.println(ch);
		//코드를 저장
		//숫자 0은 48, A는 65 ,a는 97
		ch = 65;
		System.out.println(ch);
		//소문자에서 대문자를 빼면 32
		System.out.println('a'-'A');
		
		//제어문자사용- \n은 줄바꿈
		System.out.println("안녕\n자바");
		
		
		
				
	}

}
